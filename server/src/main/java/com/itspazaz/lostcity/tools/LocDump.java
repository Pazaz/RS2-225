    public static Path dataDir = Paths.get(System.getProperty("user.dir") + "/data");
    public static Path cacheDir = Paths.get(System.getProperty("user.dir") + "/data/cache");

    public static void main(String[] args) {
        load();

        try {
            FileArchive config194 = new FileArchive(Files.readAllBytes(Paths.get(cacheDir.toString(), "config194")));
            LocType.decode(config194);
            String[] names = new String[LocType.count];
            int[] shapes = new int[LocType.count];
            Arrays.fill(shapes, -1);
            for (int i = 0; i < LocType.count; ++i) {
                LocType loc = LocType.get(i);
                names[i] = loc.name;
                if (loc.shapes.length > 0) {
                    shapes[i] = loc.shapes[0];
                }
            }

            LocType.unload();
            FileArchive config225 = new FileArchive(Files.readAllBytes(Paths.get(cacheDir.toString(), "config")));
            LocType.decode(config225);

            LocType[] loc225 = new LocType[LocType.count];
            for (int i = 0; i < LocType.count; ++i) {
                loc225[i] = LocType.get(i);
            }

            int count = 0;
            for (int i = 0; i < names.length; ++i) {
                int conv = get(i);
//                if (conv != -1 && loc225[conv].name == null) {
//                    loc225[conv].identifier = names[i];
//                }
                if (conv != -1 && names[i].toLowerCase().equals(names[i])) {
                    loc225[conv].identifier = names[i];
                    count++;
                }
                else if (conv == -1) {
//                    System.out.println(i + " " + names[i] + " " + shapes[i]);
                }
            }
            System.out.println(count);

            try {
                PrintWriter out = new PrintWriter("dump/loc.def");
                for (int i = 0; i < loc225.length; ++i) {
                    if (loc225[i].name == null) {
//                        System.out.println(i + " " + loc225[i].shapes[0]);
                    }
                    out.println(loc225[i].toJagConfig());
                }
                out.close();
            } catch (Exception ex) {
                ex.printStackTrace();
            }

//            Gson gson = new GsonBuilder()
//                .setPrettyPrinting()
//                .create();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

